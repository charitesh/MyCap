#Deleting different dictionary elements

#creating dictionary
dictdel = {1: 'hi', 2: 'hello', 3:'bye'}
print("Elements in dictionary before deleting: ", dictdel)
#removing 1st element
del dictdel[1]
print("Dictionary after removing 1st element: ", dictdel)
#removing 3rd element
del dictdel[3]
print("Dictionary after removing 3rd element: ", dictdel)
